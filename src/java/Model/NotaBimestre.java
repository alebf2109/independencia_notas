package Model;

public class NotaBimestre {
    private int curso_id;
    private String curso;
    private int n1;
    private int n2;
    private int n3;
    private int n4;

    public NotaBimestre(int curso_id, String curso, int n1, int n2, int n3, int n4) {
        this.curso_id = curso_id;
        this.curso = curso;
        this.n1 = n1;
        this.n2 = n2;
        this.n3 = n3;
        this.n4 = n4;
    }

    public int getCurso_id() {
        return curso_id;
    }

    public void setCurso_id(int curso_id) {
        this.curso_id = curso_id;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }

    public int getN1() {
        return n1;
    }

    public void setN1(int n1) {
        this.n1 = n1;
    }

    public int getN2() {
        return n2;
    }

    public void setN2(int n2) {
        this.n2 = n2;
    }

    public int getN3() {
        return n3;
    }

    public void setN3(int n3) {
        this.n3 = n3;
    }

    public int getN4() {
        return n4;
    }

    public void setN4(int n4) {
        this.n4 = n4;
    }

    public int getPromedio() {
        int suma = this.n1 + this.n2 + this.n3 + this.n4;
        return suma/4;
    }
    
}
